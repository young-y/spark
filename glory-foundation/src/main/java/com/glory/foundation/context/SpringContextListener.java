package com.glory.foundation.context;

import org.springframework.beans.BeansException;
import org.springframework.context.ApplicationContext;
import org.springframework.context.ApplicationContextAware;
import org.springframework.context.EnvironmentAware;
import org.springframework.core.env.Environment;
import org.springframework.stereotype.Component;

@Component
public class SpringContextListener implements ApplicationContextAware, EnvironmentAware {
    @Override
    public void setApplicationContext(ApplicationContext applicationContext) throws BeansException {
        SpringContext.setContext(applicationContext);
    }

	/**
	 * @param environment
	 */
	@Override
	public void setEnvironment(Environment environment) {
		SpringContext.setEnvironment(environment);
	}
}